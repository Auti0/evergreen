const rewire = require("rewire")
const DoubleChevronDownIcon = rewire("../../../../commonjs/icons/generated/DoubleChevronDownIcon")
const _interopRequireWildcard = DoubleChevronDownIcon.__get__("_interopRequireWildcard")
